using System;

// Базовый класс Товар
public class Товар
{
    public string Название { get; set; }
    public decimal Цена { get; set; }

    public void Продать()
    {
        Console.WriteLine($"Товар '{Название}' продан за {Цена} рублей.");
    }
}

// Класс Игрушка, наследующий от Товар
public class Игрушка : Товар
{
    public string Материал { get; set; }
    public int Возраст { get; set; }

    public void Играть()
    {
        Console.WriteLine($"Играем с игрушкой '{Название}' из материала '{Материал}' для детей от {Возраст} лет.");
    }
}

// Класс Продукт, наследующий от Товар
public class Продукт : Товар
{
    public DateTime СрокГодности { get; set; }

    public void Употребить()
    {
        Console.WriteLine($"Употребляем продукт '{Название}' до {СрокГодности.ToShortDateString()}.");
    }
}

// Класс Молочный продукт, наследующий от Продукт
public class МолочныйПродукт : Продукт
{
    public double Жирность { get; set; }

    public void Описать()
    {
        Console.WriteLine($"Молочный продукт '{Название}' с жирностью {Жирность}% и сроком годности до {СрокГодности.ToShortDateString()}.");
    }
}

class Program
{
    static void Main(string[] args)
    {
        var игрушка = new Игрушка { Название = "Плюшевый мишка", Материал = "Хлопок", Возраст = 3, Цена = 500 };
        игрушка.Играть();
        игрушка.Продать();

        var молочныйПродукт = new МолочныйПродукт { Название = "Молоко", Жирность = 3.2, СрокГодности = new DateTime(2023, 5, 15), Цена = 80 };
        молочныйПродукт.Описать();
        молочныйПродукт.Употребить();
        молочныйПродукт.Продать();

        Console.ReadLine();
    }
}